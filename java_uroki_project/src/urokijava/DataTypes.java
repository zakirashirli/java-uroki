package urokijava;

public class DataTypes {
    public static void main(String[] args) {
        // целые числа
        byte my_byte = -20;
        short my_short = 15000;
        int my_int = 24000;
        long my_long = 1000000;

        // дестечные числа
        float my_float = 10.4f;
        double my_double = 10.4;

        // char
        char my_char = '$';

        // boolean
        boolean my_bool = false;

        // String
        String my_str = "Hello world!";

        System.out.println(my_byte);
        System.out.println(my_short);
        System.out.println(my_int);
        System.out.println(my_long);

    }
}
